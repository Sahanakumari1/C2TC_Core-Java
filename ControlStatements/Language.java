// else-if statement
public class Language {

	public static void main(String[] args) {
		String lang = "C++";
		if(lang == "PHP") 
		{
			System.out.println("Language is PHP");
		}else if(lang == "C++")
		{
			System.out.println("Language is C++");
		}else 
		{
			System.out.println(lang);
		}
		

	}

}
